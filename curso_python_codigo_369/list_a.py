# LISTAS Y ARRAYS

lista = [1, 2, 3, "jhonas", 5.3, ["a", "b", "c"], 5]
# si queremos llamar se cuenta una desde el 0 en adelante 
# en este caso llamaremos al segundo numero que seria el 2 
print(lista[1])

# en este caso llamaremos desde un inicio a un final de la lista
# en pantalla nos muestra desde el 3 hasta jhonas
print(lista[2:4])

# si colocamos el solo el inicio imprimira desde donde iniciara 
# hasta el final 
print(lista[1:])

# si colocamos solo el final imprimira desde el inicio hasta dondes se le coloque el limite 
print(lista[:3])

# si colocamos en negatico nos imprimira desde el final hacia adelante
print(lista[-1])